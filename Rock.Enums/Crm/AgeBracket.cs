namespace Rock.Enums.Crm
{
    /// <summary>
    /// The age bracket of a person
    /// </summary>
    public enum AgeBracket
    {
        /// <summary>
        /// Unknown Age range
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Age range 0 - 12
        /// </summary>
        ZeroToTwelve = 1,

        /// <summary>
        /// Age range 13 - 17
        /// </summary>
        ThirteenToSeventeen = 2,

        /// <summary>
        /// Age range 18 - 24
        /// </summary>
        EighteenToTwentyFour = 3,

        /// <summary>
        /// Age range 25 - 34
        /// </summary>
        TwentyFiveToThirtyFour = 4,

        /// <summary>
        /// Age range 35 - 44
        /// </summary>
        ThirtyFiveToFortyFour = 5,

        /// <summary>
        /// Age range 45 - 54
        /// </summary>
        FortyFiveToFiftyFour = 6,

        /// <summary>
        /// Age range 55 - 64
        /// </summary>
        FiftyFiveToSixtyFour = 7,

        /// <summary>
        /// Age range 65+
        /// </summary>
        SixtyFiveOrOlder = 8
    }
}
